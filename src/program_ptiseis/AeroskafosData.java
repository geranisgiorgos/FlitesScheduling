    // Προσθήκη δεδομένων στον πίνακα των αεροσκαφών
    private void fillTable(){
        for(Aeroskafos a:aeroskafosList){
            addAeroskafos(a);
        }
    }
    
    public void newAeroskafos(Aeroskafos a){
        addAeroskafos(a);
        this.aeroskafosList.add(a);
    }

    public void removeAeroskafos(int row){
        this.aeroskafosList.remove(row);
        this.tableModel.removeRow(row);
        
    }
    
    // Προσθήκη γραμμής στον πίνακα
    private void addAeroskafos(Aeroskafos a){
        Vector aeroskafosRow = new Vector();
        aeroskafosRow.add(a.getSn());
        aeroskafosRow.add(a.getHoursA());
        aeroskafosRow.add(a.getHoursB());
        
        tableModel.addRow(aeroskafosRow);
    }
    
    // Εισαγωγή τροποποιημένου αεροσκάφους στον πίνακα αεροσκαφών
    public void editAeroskafos(Aeroskafos a, int row){
        int noRows=tableModel.getRowCount();
        // Διαγράφουμε την γραμμή
        tableModel.removeRow(row);
        this.aeroskafosList.remove(row);
        addAeroskafos(a);
        tableModel.moveRow(noRows-1, noRows-1, row);
        aeroskafosList.add(row, a);
    }
    
